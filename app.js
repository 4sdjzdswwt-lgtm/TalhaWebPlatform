// Rastgele 6 Haneli Doğrulama Kodu Üretici (Orijinal Mantık)
function generateSixDigitCode() {
    return Math.floor(100000 + Math.random() * 900000).toString();
}

// GİRİŞ YAPMA MOTORU
function handleLogin() {
    const email = document.getElementById('loginEmail').value.trim();
    const password = document.getElementById('loginPassword').value.trim();
    
    if(!email || !password) {
        showStatus(translations[currentLanguage].errFields, '#ff4d4d');
        return;
    }
    
    showStatus(translations[currentLanguage].msgSending, '#3a3b3c');

    auth.signInWithEmailAndPassword(email, password)
        .then((userCredential) => {
            const user = userCredential.user;
            
            // Kullanıcı bilgilerini oturum hafızasına al
            sessionStorage.setItem('active_user_email', user.email);
            sessionStorage.setItem('active_user_name', user.email.split('@')[0]);
            
            // Giriş başarılı olunca 6 haneli kod üret ve ekrana bas
            generatedVerifyCode = generateSixDigitCode();
            console.log("[GÜVENLİK KODU]: " + generatedVerifyCode); // Konsoldan görebilirsin
            alert("Doğrulama Kodunuz: " + generatedVerifyCode); 

            showStatus(translations[currentLanguage].msgLoginSuccess, '#28a745');
            
            setTimeout(() => {
                if(typeof switchStep === 'function') switchStep('verificationStep');
            }, 1000);
        })
        .catch((error) => {
            showStatus("Giriş Başarısız: " + error.message, '#ff4d4d');
        });
}
window.handleLogin = handleLogin;

// KAYIT OLMA MOTORU
function handleRegister() {
    const username = document.getElementById('regUsername').value.trim();
    const email = document.getElementById('regEmail').value.trim();
    const password = document.getElementById('regPassword').value.trim();
    
    if(!username || !email || !password) {
        showStatus(translations[currentLanguage].errFields, '#ff4d4d');
        return;
    }

    showStatus(translations[currentLanguage].msgSending, '#3a3b3c');

    auth.createUserWithEmailAndPassword(email, password)
        .then((userCredential) => {
            const user = userCredential.user;
            
            // Profili Realtime Database altına kaydetme düzeni
            const cleanEmail = email.toLowerCase().replace(/\./g, '_');
            database.ref(`users/${cleanEmail}`).set({
                username: username,
                email: email,
                createdAt: firebase.database.ServerValue.TIMESTAMP
            });

            sessionStorage.setItem('active_user_email', user.email);
            sessionStorage.setItem('active_user_name', username);
            
            // Kod üret ve doğrulama adımına aktar
            generatedVerifyCode = generateSixDigitCode();
            console.log("[GÜVENLİK KODU]: " + generatedVerifyCode);
            alert("Doğrulama Kodunuz: " + generatedVerifyCode);

            showStatus("Hesap oluşturuldu! Doğrulanıyor...", '#28a745');
            
            setTimeout(() => {
                if(typeof switchStep === 'function') switchStep('verificationStep');
            }, 1000);
        })
        .catch((error) => {
            showStatus("Kayıt Hatası: " + error.message, '#ff4d4d');
        });
}
window.handleRegister = handleRegister;

// GOOGLE İLE GİRİŞ / KAYIT MOTORU
function handleGoogleLogin() {
    auth.signInWithPopup(googleProvider)
        .then((result) => {
            const user = result.user;
            
            sessionStorage.setItem('active_user_email', user.email);
            sessionStorage.setItem('active_user_name', user.displayName || user.email.split('@')[0]);
            
            // Realtime Database kaydı kontrolü / eklemesi
            const cleanEmail = user.email.toLowerCase().replace(/\./g, '_');
            database.ref(`users/${cleanEmail}`).keepSynced(true);
            database.ref(`users/${cleanEmail}`).update({
                username: user.displayName || user.email.split('@')[0],
                email: user.email
            });

            generatedVerifyCode = generateSixDigitCode();
            console.log("[GÜVENLİK KODU]: " + generatedVerifyCode);
            alert("Doğrulama Kodunuz: " + generatedVerifyCode);

            showStatus("Google Bağlantısı Başarılı!", '#28a745');
            
            setTimeout(() => {
                if(typeof switchStep === 'function') switchStep('verificationStep');
            }, 1000);
        })
        .catch((error) => {
            showStatus("Google Hatası: " + error.message, '#ff4d4d');
        });
}
window.handleGoogleLogin = handleGoogleLogin;

// KOD DOĞRULAMA VE VİDEO SAYFASINA GEÇİŞ MOTORU
function handleVerifyCode() {
    const inputCode = document.getElementById('verificationCodeInput').value.trim();
    
    if (inputCode === generatedVerifyCode && generatedVerifyCode !== "") {
        showStatus("Kod Başarıyla Doğrulandı! Sözleşmeye geçiliyor...", '#28a745');
        setTimeout(() => {
            window.location.href = 'verify.html'; // İstediğin bağımsız onay videosu sayfası
        }, 1200);
    } else {
        showStatus(translations[currentLanguage].errCodeWrong, '#ff4d4d');
    }
}
window.handleVerifyCode = handleVerifyCode;
