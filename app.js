function toggleLanguage() {
    if (currentLanguage === 'tr') currentLanguage = 'en';
    else if (currentLanguage === 'en') currentLanguage = 'de';
    else currentLanguage = 'tr';
    
    localStorage.setItem('site_lang', currentLanguage);
    applyLanguage(currentLanguage);
}

function applyLanguage(lang) {
    const t = translations[lang];
    if(document.getElementById('langToggleBtn')) document.getElementById('langToggleBtn').innerText = t.langLbl;
    if(document.getElementById('heroDescription')) document.getElementById('heroDescription').innerText = t.txtHeroDesc;
    
    // Form Elemanlarının Dil Güncellemesi
    if(document.getElementById('loginTitle')) document.getElementById('loginTitle').innerText = t.loginTitle;
    if(document.getElementById('registerTitle')) document.getElementById('registerTitle').innerText = t.registerTitle;
    if(document.getElementById('verifyTitle')) document.getElementById('verifyTitle').innerText = t.verifyTitle;
    if(document.getElementById('verifyDesc')) document.getElementById('verifyDesc').innerText = t.verifyDesc;
    if(document.getElementById('loginBtnTxt')) document.getElementById('loginBtnTxt').innerText = t.loginBtn;
    if(document.getElementById('registerBtnTxt')) document.getElementById('registerBtnTxt').innerText = t.registerBtn;
    if(document.getElementById('verifyBtnTxt')) document.getElementById('verifyBtnTxt').innerText = t.verifyBtn;
}

function nextTheme() {
    currentThemeIndex = (currentThemeIndex + 1) % themeGifs.length;
    localStorage.setItem('talha_theme_idx', currentThemeIndex);
    applyTheme(currentThemeIndex);
}

function applyTheme(index) {
    const bgLayer = document.getElementById('gifBgLayer');
    if(bgLayer) {
        bgLayer.style.backgroundImage = `url('${themeGifs[index]}')`;
    }
}

function showStatus(text, color) {
    const box = document.getElementById('statusMessageBox');
    if(box) {
        box.innerText = text;
        box.style.background = color;
        box.style.display = 'block';
        setTimeout(() => { box.style.display = 'none'; }, 4000);
    }
}

